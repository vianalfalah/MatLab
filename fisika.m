function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

Q1 = str2double(get(handles.edit1,'string'));
Q2 = str2double(get(handles.edit3,'string'));
Q3 = str2double(get(handles.edit4,'string'));
r1 = str2double(get(handles.edit5,'string'));
r2 = str2double(get(handles.edit6,'string'));
sudut = str2double(get(handles.edit7,'string'));

k = 9*(10^9);

F2 = (k*abs(Q3)*abs(Q2))/abs(r1^2);
F1 = (k*abs(Q3)*abs(Q1))/abs(r2^2);

%menghitung total gaya coulumb sesuai soal
%sqrt = akar pangkat
%abs = nilai absolut
F3 = abs(sqrt((F1^2)+(F2^2)+(2*F1*F2)*cosd(sudut)));

%menghitung medan listrik
E3 = F3/abs(Q3);

%menghitung potensial listrik
%untuk potensial dan energi tidak perlu menggunakan abs
V3 = (k*(Q1/r1))+(k*(Q2/r2));

%menghitung energi
W3 = V3*Q3 ;

%sintak utk radiobutton
a= get(handles.radiobutton1,'value');
b= get(handles.radiobutton2,'value');
c= get(handles.radiobutton3,'value');
d= get(handles.radiobutton4,'value');

if (a==1)
    set(handles.edit2,'string',F3);
elseif (b==1)
        set(handles.edit2,'string',E3);
elseif (c==1)
        set(handles.edit2,'string',V3);
else
    set(handles.edit2,'string',W3);
        end
        
    



